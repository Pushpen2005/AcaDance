#!/bin/bash

# Render Deployment Script for Academic System
# This script helps prepare and deploy your application to Render

set -e  # Exit on any error

echo "üé® Academic System - Render Deployment Script"
echo "============================================="

# Function to handle cleanup on exit
cleanup() {
    if [ $? -ne 0 ]; then
        echo ""
        echo "‚ùå Deployment failed! Check the errors above."
        echo "üí° Common fixes:"
        echo "   - Ensure all environment variables are set"
        echo "   - Check your git repository is properly configured"
        echo "   - Verify your code builds locally without errors"
        echo "   - Make sure you're connected to the internet"
    fi
}

trap cleanup EXIT

# Check if Node.js and npm are available
if ! command -v node &> /dev/null; then
    echo "‚ùå Node.js not found. Please install Node.js first."
    exit 1
fi

if ! command -v npm &> /dev/null; then
    echo "‚ùå npm not found. Please install npm first."
    exit 1
fi

echo "‚úÖ Node.js $(node --version) and npm $(npm --version) found"

# Check for required environment variables
echo ""
echo "üîç Checking environment variables..."

required_vars=(
    "NEXT_PUBLIC_SUPABASE_URL"
    "NEXT_PUBLIC_SUPABASE_ANON_KEY" 
    "SUPABASE_SERVICE_ROLE_KEY"
    "NEXT_PUBLIC_SITE_URL"
)

missing_vars=()

for var in "${required_vars[@]}"; do
    if [ -z "${!var}" ]; then
        missing_vars+=("$var")
        echo "‚ùå $var is not set"
    else
        echo "‚úÖ $var is set"
    fi
done

if [ ${#missing_vars[@]} -gt 0 ]; then
    echo ""
    echo "‚ö†Ô∏è  Missing environment variables detected!"
    echo "Please set the following variables before deployment:"
    for var in "${missing_vars[@]}"; do
        echo "   - $var"
    done
    echo ""
    echo "You can:"
    echo "1. Set them in your Render dashboard under Environment section"
    echo "2. Create a .env.local file for local testing (not recommended for production)"
    echo "3. Export them in your current shell session"
    echo ""
    read -p "Continue with deployment anyway? (y/N): " -n 1 -r
    echo ""
    if [[ ! $REPLY =~ ^[Yy]$ ]]; then
        echo "Deployment cancelled. Please set the required environment variables."
        exit 1
    fi
else
    echo "‚úÖ All required environment variables are set"
fi

# Check if this is a git repository
echo ""
echo "üîç Checking git repository status..."
if [ ! -d ".git" ]; then
    echo "‚ùå This is not a git repository!"
    echo "Render requires a git repository for deployment."
    echo "Please initialize git and push to a remote repository first:"
    echo ""
    echo "   git init"
    echo "   git add ."
    echo "   git commit -m 'Initial commit'"
    echo "   git remote add origin <your-repo-url>"
    echo "   git push -u origin main"
    echo ""
    exit 1
else
    echo "‚úÖ Git repository found"
fi

# Check if we have a remote repository
if ! git remote -v | grep -q "origin"; then
    echo "‚ùå No remote origin found. Render requires a remote repository."
    echo "Please add a remote origin:"
    echo "   git remote add origin <your-repo-url>"
    echo "   git push -u origin main"
    exit 1
else
    echo "‚úÖ Remote repository found"
fi

# Check for uncommitted changes
if [ -n "$(git status --porcelain)" ]; then
    echo ""
    echo "‚ö†Ô∏è  You have uncommitted changes:"
    git status --short
    echo ""
    read -p "Commit and push changes before deployment? (Y/n): " -n 1 -r
    echo ""
    if [[ ! $REPLY =~ ^[Nn]$ ]]; then
        echo "üìù Committing changes..."
        git add .
        if git commit -m "Pre-deployment commit - $(date)"; then
            echo "‚úÖ Changes committed"
            echo "üì§ Pushing to remote repository..."
            if git push; then
                echo "‚úÖ Changes pushed successfully"
            else
                echo "‚ùå Failed to push changes"
                exit 1
            fi
        else
            echo "‚ùå Failed to commit changes"
            exit 1
        fi
    else
        echo "‚ö†Ô∏è  Continuing with uncommitted changes..."
        echo "üì§ Pushing existing commits to remote..."
        if ! git push; then
            echo "‚ùå Failed to push to remote repository"
            exit 1
        fi
    fi
else
    echo "‚úÖ No uncommitted changes"
    echo "üì§ Ensuring remote is up to date..."
    if ! git push; then
        echo "‚ùå Failed to push to remote repository"
        exit 1
    fi
fi

# Install dependencies if needed
echo ""
echo "üì¶ Checking dependencies..."
if [ ! -d "node_modules" ]; then
    echo "Installing dependencies..."
    if [ -f "pnpm-lock.yaml" ]; then
        if command -v pnpm &> /dev/null; then
            pnpm install
        else
            echo "‚ùå pnpm not found but pnpm-lock.yaml exists. Installing pnpm..."
            npm install -g pnpm
            pnpm install
        fi
    elif [ -f "yarn.lock" ]; then
        if command -v yarn &> /dev/null; then
            yarn install
        else
            echo "‚ùå yarn not found but yarn.lock exists. Please install yarn or use npm."
            npm install
        fi
    else
        npm install
    fi
    echo "‚úÖ Dependencies installed"
else
    echo "‚úÖ Dependencies already installed"
fi

# Build and test locally first
echo ""
echo "üî® Building project locally..."
echo "This may take a few minutes..."

if npm run build; then
    echo "‚úÖ Local build successful"
else
    echo "‚ùå Local build failed! Please fix the following issues:"
    echo "1. Check for TypeScript errors"
    echo "2. Verify all imports are correct"
    echo "3. Ensure all environment variables are set"
    echo "4. Check for syntax errors in your code"
    exit 1
fi

# Create render.yaml if it doesn't exist
if [ ! -f "render.yaml" ]; then
    echo ""
    echo "üìÑ Creating render.yaml configuration file..."
    cat > render.yaml << 'EOF'
services:
  - type: web
    name: academic-system
    env: node
    plan: free
    buildCommand: npm install && npm run build
    startCommand: npm start
    healthCheckPath: /api/health
    envVars:
      - key: NODE_VERSION
        value: 18
      - key: NODE_ENV
        value: production
    domains:
      - academic-system.onrender.com
EOF
    echo "‚úÖ render.yaml created with default configuration"
else
    echo "‚úÖ render.yaml already exists"
fi

# Create Dockerfile for Render if it doesn't exist
if [ ! -f "Dockerfile.render" ]; then
    echo ""
    echo "üìÑ Creating Dockerfile.render for optimal Render deployment..."
    cat > Dockerfile.render << 'EOF'
# Use the official Node.js runtime as the base image
FROM node:18-alpine

# Set the working directory in the container
WORKDIR /app

# Copy package.json and package-lock.json (if available)
COPY package*.json ./

# Install dependencies
RUN npm ci --only=production

# Copy the rest of the application code
COPY . .

# Build the Next.js application
RUN npm run build

# Expose the port the app runs on
EXPOSE 3000

# Define the command to run the application
CMD ["npm", "start"]
EOF
    echo "‚úÖ Dockerfile.render created for containerized deployment"
else
    echo "‚úÖ Dockerfile.render already exists"
fi

# Show deployment instructions
echo ""
echo "üöÄ Render Deployment Instructions"
echo "================================="
echo ""
echo "Render deploys automatically from your Git repository."
echo "Follow these steps to complete your deployment:"
echo ""
echo "üìã STEP-BY-STEP DEPLOYMENT:"
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
echo ""
echo "1. üåê Go to Render Dashboard:"
echo "   https://dashboard.render.com"
echo ""
echo "2. üîê Sign up/Login with your Git provider (GitHub/GitLab/Bitbucket)"
echo ""
echo "3. ‚ûï Create a New Web Service:"
echo "   - Click 'New +' > 'Web Service'"
echo "   - Connect your repository: $(git remote get-url origin 2>/dev/null || echo 'your-repo-url')"
echo "   - Select the branch: $(git branch --show-current 2>/dev/null || echo 'main')"
echo ""
echo "4. ‚öôÔ∏è  Configure your service:"
echo "   - Name: academic-system"
echo "   - Environment: Node"
echo "   - Build Command: npm install && npm run build"
echo "   - Start Command: npm start"
echo "   - Plan: Free (or choose your preferred plan)"
echo ""
echo "5. üîß Set Environment Variables:"
echo "   Add these in the Environment Variables section:"
for var in "${required_vars[@]}"; do
    if [ -n "${!var}" ]; then
        echo "   - $var = ${!var}"
    else
        echo "   - $var = <SET_YOUR_VALUE>"
    fi
done
echo "   - NODE_ENV = production"
echo "   - PORT = 3000"
echo ""
echo "6. üöÄ Deploy:"
echo "   - Click 'Create Web Service'"
echo "   - Render will automatically deploy from your repository"
echo "   - Watch the build logs for any issues"
echo ""
echo "üìã Post-deployment checklist:"
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
echo "üîß REQUIRED ACTIONS:"
echo "1. üîê Update Supabase Auth settings:"
echo "   - Go to: https://app.supabase.com/project/[your-project]/auth/url-configuration"
echo "   - Add your Render URL to 'Site URL' and 'Redirect URLs'"
echo "   - Format: https://academic-system.onrender.com"
echo ""
echo "2. üîÑ Set up automatic deployments:"
echo "   - Render automatically deploys on git push"
echo "   - Configure branch auto-deploy in Render settings"
echo ""
echo "3. üåç Configure custom domain (optional):"
echo "   - Go to Render service settings > Custom Domains"
echo "   - Add your custom domain and configure DNS"
echo ""
echo "üß™ TESTING CHECKLIST:"
echo "4. ‚úÖ Test authentication flow (login/logout)"
echo "5. ‚úÖ Verify database connections work"
echo "6. ‚úÖ Check all API endpoints respond correctly"
echo "7. ‚úÖ Test real-time features (if applicable)"
echo "8. ‚úÖ Verify file uploads work (if applicable)"
echo "9. ‚úÖ Test mobile responsiveness"
echo ""
echo "üîó Useful links:"
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
echo "   üìä Render Dashboard: https://dashboard.render.com"
echo "   üóÑÔ∏è  Supabase Dashboard: https://app.supabase.com"
echo "   üìö Render Docs: https://render.com/docs"
echo ""
echo "üéâ Your code is ready for Render deployment!"
echo "üí° Follow the steps above to complete your deployment"

# Reset trap to avoid showing error message on successful completion
trap - EXIT
